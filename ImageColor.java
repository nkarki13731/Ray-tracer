public class ImageColor {
    public int getB() {
        return b;
    }

    public int getG() {
        return g;
    }

    public int getR() {
        return r;
    }

    private int r;

    private int g;
    private int b;

    public ImageColor(int r, int g, int b){
        this.r = r;
        this.b = b;
        this.g = g;
    }

}
